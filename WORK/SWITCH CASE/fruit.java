public class fruit {    
public static void main(String[] args) {  
int fruit = 2;
switch (fruit) {
  case 1:
    System.out.println("papaya");
    break;
  case 2:
    System.out.println("banana");
    break;
  case 3:
    System.out.println("watermelon");
    break;
  case 4:
    System.out.println("cherry");
    break;
}
}
}